bw_img = None

def open_img():
    global bw_img
    bw_img = filedialog.askopenfilename(initialdir=os.getcwd(), title="Select Image File", filetypes=(("JPG FILE", "*.jpg"),("PNG FILE", "*.png"),("ALL Files", "*.*")))
    photo = ImageTk.PhotoImage(Image.open(bw_img))
    dis = Label(root, image=photo, relief="solid").place(x=290, y=100, width=224, height=224)
    dis.pack(fill=None)


def classify():
    model = load_model(
        'weights_xception.h5', compile=True)
    
    img = image.load_img(bw_img, target_size=(224, 224))
    img = np.array(img)
    img = np.expand_dims(img, axis=0)
    img = preprocess_input(img)                                      

    pred = model.predict(img)

    index = np.argmax(pred)

    prediction = species[index]
    Label(root, text=prediction,relief="solid").place(x=350,y=350)

root = Tk()
root.title('Bird Species Classifier')
root.geometry("800x700")
root.iconbitmap('static/icon.ico')
tit = Label(root, text="Bird Species Classifier", padx=25, pady=6, font=('', 17,'bold')).pack()


Button(root, text='Choose Image',
        padx=35, pady=10, font=('',10),
        fg="white", bg="grey", command=open_img).place(x=160,y=450)
Button(root, text='Classify Image',
        padx=35, pady=10, font=('',10),
        fg="white", bg="grey", command=classify).place(x=480,y=450)
Button(root, text='Exit',
        padx=35, pady=10, font=('',10),
        fg="white", bg="grey", command=root.destroy).place(x=350,y=550)

root.resizable(0,0)
root.mainloop()
